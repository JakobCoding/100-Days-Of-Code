# Subscripting - Prints the letter at 0 index
# prints first letter
print("Hello"[0])

# prints last letter 
print("Hello"[-1])

# Integer = Whole number
print(123 + 456)

# Floats - decimal point numbers (floating point number)
print(3.14159)

# Boolean - True or False values only 
print(type(True))
print(False)

# converting data types 
print(len(str(12345)))

# checking data types
print(type("Hello"))